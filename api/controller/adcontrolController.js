const AdControl = require("../model/adcontrolModel.js");


// Retrieve all Advertisement from the database (without condition).
exports.findAll = (req, res) => {
    AdControl.getApplys((err, data) => {
        if (err)
            res.status(500).send({
                message:
                    err.message || "Some error occurred while retrieving tutorials."
            });
        else res.send(data);
    });
};

// Find a single Advertisements with a id
exports.findOne = (req, res) => {
    AdControl.getApply(req.params.id, (err, data) => {
        if (err) {
            if (err.kind === "not_found") {
                res.status(404).send({
                    message: `Not found Tutorial with id ${req.params.id}.`
                });
            } else {
                res.status(500).send({
                    message: "Error retrieving Tutorial with id " + req.params.id
                });
            }
        } else res.send(data);
    });
};
